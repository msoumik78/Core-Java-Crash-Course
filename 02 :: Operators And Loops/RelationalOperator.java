public class RelationalOperator {
    public static void main(String[] args) {
        int firstNumber = 40;
        int secondNumber = 10;

        // Output of relational operator is always a boolean (true or false)
        System.out.println("Is first number equal to second number =" + (firstNumber == secondNumber));
        System.out.println("Is first number greater than second number =" +(firstNumber > secondNumber));
        System.out.println("Is first number lesser than second number =" +(firstNumber < secondNumber));
    }
}
